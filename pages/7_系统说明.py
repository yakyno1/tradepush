from __future__ import annotations

import streamlit as st

from tradepush.ui.components import hero, section
from tradepush.ui.theme import setup_page

setup_page("系统说明", "◇")
hero("系统说明与交易纪律", "TradePush的目标不是预测所有上涨，而是系统性过滤不能买、不能追和不能补仓的情形。")

section("三层决策")
st.markdown(
    """
1. **市场开关**：进攻、谨慎、停止新开仓。
2. **板块轮动**：主线进攻、轮动观察、防守方向、权重护盘、退潮回避。
3. **个股条件**：核心地位、相对强度、入场位置、量价承接、失效位、收益风险比。
"""
)

section("硬纪律")
st.markdown(
    """
- 单笔最大计划亏损默认不超过账户权益的1%。
- 单票不超过20%，同主题不超过35%，总仓位不超过85%。
- 收益风险比低于2、止损距离超过8%、数据异常或市场关闭时禁止买入。
- 评分只排序，不能抵消硬否决。
- AI可以否决或降级，不能扩大仓位、移动止损或绕过门槛。
- 第一版只生成订单草案，不连接券商。
"""
)

section("使用顺序")
st.markdown(
    """
1. 在“数据中心与设置”确认账户、自选池和安全区。
2. 在“今日总览”运行分析。
3. 在“板块轮动”确认主线性质。
4. 在“个股决策”检查价格触发和硬门槛。
5. 对拟买入/加仓候选生成AI双审包。
6. 人工下单，并在收盘后记录和复盘。
"""
)

